"""
求钱包余额
"""
money = 50
# 当前钱包余额：50
print("当前钱包余额:", money)
# 购买了冰淇淋，花费：10
print("购买了冰淇淋，花费：", 10, "元")
# 购买了可乐，花费：5
print("购买了可乐，花费：", 5, "元")
money = money - 10 - 5
# 最终，钱包剩余：35
print("最终，钱包剩余：", money, "元")
print(type("黑马程序员"))
# 变量是没有类型的，数据才有类型
print(type("黑马程序员月薪过万"))
type(1)
print(money)

